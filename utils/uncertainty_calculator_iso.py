# utils/uncertainty_calculator_iso.py

import numpy as np
import pandas as pd


def add_iso_uncertainty_columns(
    df_iso: pd.DataFrame,
    u_dp_pct: float = 1.0,     # 差圧計の相対標準不確かさ [%]
    u_D_pct: float = 0.1,      # 配管径 D の相対標準不確かさ [%]
    u_d_pct: float = 0.1,      # オリフィス径 d の相対標準不確かさ [%]
    u_rho_pct: float = 0.5,    # 密度 ρ の相対標準不確かさ [%]
) -> pd.DataFrame:
    """
    ISO5167 Corner Tap の 10点計算結果 df_iso に
    不確かさ列を追加する簡易 GUM モデル。

    Parameters
    ----------
    df_iso : DataFrame
        ISO5167 Corner Tap の計算結果（推奨テンプレート構造）
    u_xx_pct : float
        各入力の相対標準不確かさ [%]

    Returns
    -------
    DataFrame
        不確かさ列を追加した DataFrame
    """

    df = df_iso.copy()

    # ---------------------------------------------------------
    # 必須列チェック
    # ---------------------------------------------------------
    required_cols = [
        "差圧[kPa]",
        "体積流量[m³/h]",
        "流出係数C",
        "膨張補正係数ε",
        "密度ρ[kg/m³]",
        "内径D[mm]",
        "孔径d[mm]",
    ]

    for col in required_cols:
        if col not in df.columns:
            raise ValueError(f"不確かさ計算に必要な列がありません: {col}")

    # ---------------------------------------------------------
    # 相対標準不確かさ [% → 小数]
    # ---------------------------------------------------------
    u_dp_rel = u_dp_pct / 100.0
    u_D_rel = u_D_pct / 100.0
    u_d_rel = u_d_pct / 100.0
    u_rho_rel = u_rho_pct / 100.0

    # ---------------------------------------------------------
    # 感度係数（ISO5167 の流量式に基づく簡易モデル）
    # ---------------------------------------------------------
    a_dp = 0.5   # ΔP の感度係数
    a_D = 2.0    # D の感度係数
    a_d = 2.0    # d の感度係数
    a_rho = 0.5  # ρ の感度係数

    # ---------------------------------------------------------
    # 合成相対標準不確かさ（全点共通）
    # ---------------------------------------------------------
    u_Q_rel = np.sqrt(
        (a_dp * u_dp_rel) ** 2 +
        (a_D * u_D_rel) ** 2 +
        (a_d * u_d_rel) ** 2 +
        (a_rho * u_rho_rel) ** 2
    )

    # ---------------------------------------------------------
    # 各点の流量 Q
    # ---------------------------------------------------------
    Q = df["体積流量[m³/h]"].astype(float)

    # ---------------------------------------------------------
    # 標準不確かさ [%]
    # ---------------------------------------------------------
    df["標準不確かさ[%]"] = u_Q_rel * 100.0

    # ---------------------------------------------------------
    # 拡張不確かさ（k=2）
    # ---------------------------------------------------------
    k = 2.0
    u_Q_exp_rel = u_Q_rel * k

    df["拡張不確かさ[%]"] = u_Q_exp_rel * 100.0
    df["拡張不確かさ[m3/h]"] = Q * u_Q_exp_rel

    # ---------------------------------------------------------
    # 信頼区間
    # ---------------------------------------------------------
    df["信頼区間下限[m3/h]"] = Q - df["拡張不確かさ[m3/h]"]
    df["信頼区間上限[m3/h]"] = Q + df["拡張不確かさ[m3/h]"]

    # ---------------------------------------------------------
    # 有効自由度・カバレッジ係数（簡易モデル）
    # ---------------------------------------------------------
    df["有効自由度"] = 50.0
    df["カバレッジ係数"] = k

    return df
